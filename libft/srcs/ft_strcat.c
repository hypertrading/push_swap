#include "libft.h"

char	*ft_strcat(char *dst, const char *src)
{
	unsigned int i;
	unsigned int j;

	i = ft_strlen(dst);
	j = 0;
	while (src[j] != '\0')
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (dst);
}
